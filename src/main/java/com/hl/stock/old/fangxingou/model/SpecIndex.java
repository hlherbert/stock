package com.hl.stock.old.fangxingou.model;

/**
 * 规格索引
 */
public class SpecIndex {
    /**
     * 规格ID
     */
    private Long id;
    /**
     * 规格名称
     */
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
